
public class main {
	
	public static void main(String[] args) {
		
		int x; // Declaration
		x = 123; // Assignment
		int y = 456; // Initialization
		
		System.out.println(x);
		System.out.println("My number is: "+y);
		
		long z = 12345678987654321L; // L to extremely long numbers
		
		float pi = 3.14f;
		double phi = 3.141592653589793;
		
		boolean a = true;
		boolean b = false;
		
		char symbol = '@';
		System.out.println(symbol);
		
		String name = "Rian";
		System.out.println("Hello " + name);
	}
}
